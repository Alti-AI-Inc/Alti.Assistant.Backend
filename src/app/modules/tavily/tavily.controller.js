import { GoogleGenAI } from '@google/genai';
import httpStatus from 'http-status';
import config from '../../../../config/index.js';
import catchAsync from '../../../shared/catchAsync.js';
import sendResponse from '../../../shared/sendResponse.js';
import generateSessionId from '../../../shared/sessionGenerate.js';
import UserModel from '../auth/auth.model.js';
import Llama from '../groq/groq.model.js';

const ai = new GoogleGenAI({ apiKey: config.gemini_secret_key });

const TavilyAiGetResponseAnonymously = catchAsync(async (req, res) => {
  const prompt = req.body?.prompt;
  const userId = req.body?.user;
  const sessionId = req.body?.sessionId;
  const currentSessionId = sessionId || generateSessionId(24);

  if (!prompt) {
    return sendResponse(res, {
      statusCode: httpStatus.BAD_REQUEST,
      success: false,
      message: 'Validation Error',
      errorMessages: [{ path: 'prompt', message: 'Prompt is required.' }],
    });
  }

  const user = await UserModel.findById(userId);
  if (!user) {
    return sendResponse(res, {
      statusCode: httpStatus.NOT_FOUND,
      success: false,
      message: 'User not found.',
    });
  }

  try {
    // Use Gemini with Google Search Grounding — replaces Tavily + Groq
    const result = await ai.models.generateContent({
      model: 'gemini-3.5-flash',
      contents: prompt,
      config: {
        temperature: 0.1,
        tools: [{ googleSearch: {} }],
      },
    });

    const candidate = result.candidates?.[0];
    const reply = candidate?.content?.parts
      ?.filter((part) => part.text && !part.thought)
      ?.map((part) => part.text)
      ?.join('') || 'No reply generated';

    if (!reply || reply === 'No reply generated') {
      return sendResponse(res, {
        statusCode: httpStatus.BAD_REQUEST,
        success: false,
        message: 'Validation Error',
        errorMessages: [
          {
            path: 'message',
            message: 'Reply could not be generated by the AI model.',
          },
        ],
      });
    }

    const responseData = {
      prompt,
      model: 'gemini-3.5-flash-grounded',
      reply,
      total_time: result.usageMetadata?.totalTokenCount || 0,
    };

    let llamaSession = await Llama.findOne({
      user: userId,
      sessionId: currentSessionId,
    });

    if (llamaSession) {
      llamaSession.responses.push(responseData);
      await llamaSession.save();
    } else {
      llamaSession = await Llama.create({
        user: userId,
        sessionId: currentSessionId,
        responses: [responseData],
      });

      await UserModel.findByIdAndUpdate(userId, {
        $push: { llamaAiSessions: llamaSession._id },
      });
    }

    sendResponse(res, {
      statusCode: httpStatus.OK,
      success: true,
      message: 'Response processed successfully.',
      data: { sessionId: currentSessionId, reply },
    });
  } catch (error) {
    console.error('Error:', error.message);
    return sendResponse(res, {
      statusCode: httpStatus.INTERNAL_SERVER_ERROR,
      success: false,
      message: 'AI model processing failed.',
    });
  }
});

export const TavilyAiController = {
  TavilyAiGetResponseAnonymously,
};
